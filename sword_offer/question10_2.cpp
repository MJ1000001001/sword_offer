/*一只青蛙一次可以跳上1级台阶，也可以跳上2级……它也可以跳上n级。求该青蛙跳上一个n级的台阶总共有多少种跳法。*/
#include "question.h"

using namespace std;

class Solution {
public:
	int jumpFloorII(int number) {
		if (number == 1)
			return 1;
		int i = 1;
		int sum = 1;
		for (; i < number; i++)
			sum += jumpFloorII(i);
		return sum;
	}
};
void question10_2()
{
	Solution s;
	int n = 0;
	cin >> n;
	cout << s.jumpFloorII(n) << endl;
}